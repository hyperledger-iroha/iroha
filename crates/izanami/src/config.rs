/// Command-line arguments exposed by the `izanami` binary.
#[derive(Debug, Parser, Clone)]
#[command(author, version, about = "Izanami chaosnet orchestrator for Iroha", long_about = None)]
pub struct IzanamiArgs {
    /// Launch interactive TUI to configure a run.
    #[arg(long)]
    pub tui: bool,
    /// Allow Izanami to orchestrate networked peers (required outside tests).
    #[arg(long, default_value_t = false)]
    pub allow_net: bool,
    /// Total number of peers to start in the chaos network.
    #[arg(long, default_value_t = 4)]
    pub peers: usize,
    /// Number of peers that should behave in a faulty (Byzantine) manner.
    #[arg(long, default_value_t = 1)]
    pub faulty: usize,
    /// Wall-clock duration for which the scenario should run.
    #[arg(long, default_value = "120s", value_parser = parse_duration)]
    pub duration: Duration,
    /// Optional total consensus pipeline time (block production + commit), minimum 2ms.
    #[arg(long, value_parser = parse_duration)]
    pub pipeline_time: Option<Duration>,
    /// Target total block height required across all running peers before stopping.
    #[arg(long)]
    pub target_blocks: Option<u64>,
    /// Interval between block-height progress checks when a target is set.
    #[arg(long, default_value = "15s", value_parser = parse_duration)]
    pub progress_interval: Duration,
    /// Maximum time without block progress before failing when a target is set.
    #[arg(long, default_value = "120s", value_parser = parse_duration)]
    pub progress_timeout: Duration,
    /// Optional p95 block-interval threshold enforced when `target_blocks` is set.
    #[arg(long, value_parser = parse_duration)]
    pub latency_p95_threshold: Option<Duration>,
    /// Optional deterministic seed for reproducible chaos runs.
    #[arg(long)]
    pub seed: Option<u64>,
    /// Target average transactions per second generated by Izanami.
    #[arg(long, default_value_t = 15.0)]
    pub tps: f64,
    /// Upper bound on the number of concurrently in-flight transactions submitted by Izanami.
    #[arg(long, default_value_t = 32)]
    pub max_inflight: usize,
    /// Workload profile controlling which recipes are scheduled.
    #[arg(long, value_enum, default_value_t = WorkloadProfile::Stable)]
    pub workload_profile: WorkloadProfile,
    /// Allow contract deployment recipes in stable runs (use with care).
    #[arg(long, default_value_t = false)]
    pub allow_contract_deploy_in_stable: bool,
    /// Tracing filter passed to `tracing-subscriber` (overridden by `RUST_LOG` if set).
    #[arg(long, default_value = "info")]
    pub log_filter: String,
    /// Minimum interval between fault injections against a faulty peer.
    #[arg(long, default_value = "5s", value_parser = parse_duration)]
    pub fault_interval_min: Duration,
    /// Maximum interval between fault injections against a faulty peer.
    #[arg(long, default_value = "20s", value_parser = parse_duration)]
    pub fault_interval_max: Duration,
    /// Fault toggle switches parsed from CLI flags.
    #[command(flatten)]
    pub faults: FaultArgs,
    /// Enable Nexus/Sora multi-lane defaults from `defaults/nexus/config.toml`.
    #[arg(long)]
    pub nexus: bool,
}

/// Workload profiles for recipe selection.
#[derive(Debug, Clone, Copy, ValueEnum, PartialEq, Eq, Default)]
pub enum WorkloadProfile {
    /// Favor deterministic, execution-safe recipes for long runs.
    #[default]
    Stable,
    /// Include intentionally invalid recipes for chaos coverage.
    Chaos,
}

pub const DEFAULT_PROGRESS_INTERVAL: Duration = Duration::from_secs(15);
pub const DEFAULT_PROGRESS_TIMEOUT: Duration = Duration::from_secs(120);
/// Minimum pipeline time accepted by the test-network builder (must stay in sync).
pub const MIN_PIPELINE_TIME: Duration = Duration::from_millis(2);

/// CLI fault toggles controlling which fault injectors run.
#[derive(Debug, Clone, Args)]
#[allow(clippy::struct_excessive_bools)]
pub struct FaultArgs {
    /// Enable transient network latency faults.
    #[arg(long = "fault-enable-network-latency", default_value_t = true)]
    pub network_latency: bool,
    /// Enable network partition faults.
    #[arg(long = "fault-enable-network-partition", default_value_t = true)]
    pub network_partition: bool,
    /// Enable CPU stress faults.
    #[arg(long = "fault-enable-cpu-stress", default_value_t = true)]
    pub cpu_stress: bool,
    /// Enable disk saturation faults.
    #[arg(long = "fault-enable-disk-saturation", default_value_t = true)]
    pub disk_saturation: bool,
}

impl FaultArgs {
    pub fn to_toggles(&self) -> FaultToggles {
        FaultToggles::from_array([
            self.network_latency,
            self.network_partition,
            self.cpu_stress,
            self.disk_saturation,
        ])
    }
}

impl Default for FaultArgs {
    fn default() -> Self {
        Self {
            network_latency: true,
            network_partition: true,
            cpu_stress: true,
            disk_saturation: true,
        }
    }
}

impl From<FaultToggles> for FaultArgs {
    fn from(toggles: FaultToggles) -> Self {
        Self {
            network_latency: toggles.network_latency(),
            network_partition: toggles.network_partition(),
            cpu_stress: toggles.cpu_stress(),
            disk_saturation: toggles.disk_saturation(),
        }
    }
}

/// Bitflags describing which fault injectors are enabled.
#[derive(Debug, Clone, Copy, Default, PartialEq, Eq)]
pub struct FaultToggles {
    bits: u8,
}

impl FaultToggles {
    const NETWORK_LATENCY: u8 = 1 << 0;
    const NETWORK_PARTITION: u8 = 1 << 1;
    const CPU_STRESS: u8 = 1 << 2;
    const DISK_SATURATION: u8 = 1 << 3;

    pub const fn from_array(flags: [bool; 4]) -> Self {
        let mut bits = 0u8;
        if flags[0] {
            bits |= Self::NETWORK_LATENCY;
        }
        if flags[1] {
            bits |= Self::NETWORK_PARTITION;
        }
        if flags[2] {
            bits |= Self::CPU_STRESS;
        }
        if flags[3] {
            bits |= Self::DISK_SATURATION;
        }
        Self { bits }
    }

    pub const fn from_bits(bits: u8) -> Self {
        Self { bits: bits & 0x0F }
    }

    pub const fn bits(self) -> u8 {
        self.bits
    }

    pub const fn network_latency(self) -> bool {
        self.bits & Self::NETWORK_LATENCY != 0
    }

    pub const fn network_partition(self) -> bool {
        self.bits & Self::NETWORK_PARTITION != 0
    }

    pub const fn cpu_stress(self) -> bool {
        self.bits & Self::CPU_STRESS != 0
    }

    pub const fn disk_saturation(self) -> bool {
        self.bits & Self::DISK_SATURATION != 0
    }
}

/// Derived runtime configuration constructed from [`IzanamiArgs`].
#[derive(Debug, Clone)]
pub struct ChaosConfig {
    /// Whether Izanami may open network sockets; must be true to run scenarios.
    pub allow_net: bool,
    pub peer_count: usize,
    pub faulty_peers: usize,
    pub duration: Duration,
    pub pipeline_time: Option<Duration>,
    pub target_blocks: Option<u64>,
    pub progress_interval: Duration,
    pub progress_timeout: Duration,
    pub latency_p95_threshold: Option<Duration>,
    pub seed: Option<u64>,
    pub tps: f64,
    pub max_inflight: usize,
    pub workload_profile: WorkloadProfile,
    pub allow_contract_deploy_in_stable: bool,
    pub fault_interval: RangeInclusive<Duration>,
    pub log_filter: String,
    pub faults: FaultToggles,
    pub nexus: Option<NexusProfile>,
}

impl TryFrom<IzanamiArgs> for ChaosConfig {
    type Error = color_eyre::Report;

    fn try_from(args: IzanamiArgs) -> Result<Self> {
        if !args.allow_net {
            return Err(eyre!(
                "network orchestration disabled: pass --allow-net or set allow_net=true to run Izanami"
            ));
        }
        if args.peers == 0 {
            return Err(eyre!("peer count must be greater than zero"));
        }
        if args.faulty > args.peers {
            return Err(eyre!(
                "faulty peer count ({}) cannot exceed total peer count ({})",
                args.faulty,
                args.peers
            ));
        }
        if args.duration.is_zero() {
            return Err(eyre!("duration must be greater than zero"));
        }
        if args
            .pipeline_time
            .is_some_and(|duration| duration < MIN_PIPELINE_TIME)
        {
            return Err(eyre!(
                "pipeline_time must be at least {} ms",
                MIN_PIPELINE_TIME.as_millis()
            ));
        }
        if args.target_blocks == Some(0) {
            return Err(eyre!("target_blocks must be greater than zero"));
        }
        if args.progress_interval.is_zero() {
            return Err(eyre!("progress_interval must be greater than zero"));
        }
        if args.progress_timeout.is_zero() {
            return Err(eyre!("progress_timeout must be greater than zero"));
        }
        if args.progress_interval > args.progress_timeout {
            return Err(eyre!(
                "progress_interval ({:?}) must not exceed progress_timeout ({:?})",
                args.progress_interval,
                args.progress_timeout
            ));
        }
        if args
            .latency_p95_threshold
            .is_some_and(|duration| duration.is_zero())
        {
            return Err(eyre!("latency_p95_threshold must be greater than zero"));
        }
        if args.latency_p95_threshold.is_some() && args.target_blocks.is_none() {
            return Err(eyre!(
                "latency_p95_threshold requires target_blocks to be set"
            ));
        }
        if args.tps <= 0.0 {
            return Err(eyre!("tps must be positive"));
        }
        if !args.tps.is_finite() {
            return Err(eyre!("tps must be finite"));
        }
        let interval_secs = 1.0 / args.tps;
        let interval = Duration::try_from_secs_f64(interval_secs)
            .map_err(|_| eyre!("tps too low for timer range"))?;
        if interval.is_zero() {
            return Err(eyre!("tps too high for timer resolution"));
        }
        if args.max_inflight == 0 {
            return Err(eyre!("max_inflight must be greater than zero"));
        }
        if args.fault_interval_min > args.fault_interval_max {
            return Err(eyre!(
                "fault interval min ({:?}) cannot be greater than max ({:?})",
                args.fault_interval_min,
                args.fault_interval_max,
            ));
        }

        let toggles = args.faults.to_toggles();
        let IzanamiArgs {
            tui: _,
            peers,
            faulty,
            duration,
            pipeline_time,
            target_blocks,
            progress_interval,
            progress_timeout,
            latency_p95_threshold,
            seed,
            tps,
            max_inflight,
            workload_profile,
            allow_contract_deploy_in_stable,
            log_filter,
            fault_interval_min,
            fault_interval_max,
            faults: _faults,
            nexus,
            allow_net,
        } = args;
        let nexus = if nexus {
            Some(NexusProfile::sora_defaults()?)
        } else {
            None
        };

        Ok(Self {
            allow_net,
            peer_count: peers,
            faulty_peers: faulty,
            duration,
            pipeline_time,
            target_blocks,
            progress_interval,
            progress_timeout,
            latency_p95_threshold,
            seed,
            tps,
            max_inflight,
            workload_profile,
            allow_contract_deploy_in_stable,
            fault_interval: fault_interval_min..=fault_interval_max,
            log_filter,
            faults: toggles,
            nexus,
        })
    }
}

/// Initialise tracing using the resolved filter string.
pub fn init_tracing_with_filter(default_filter: &str) {
    static SUBSCRIBER: OnceLock<()> = OnceLock::new();
    SUBSCRIBER.get_or_init(|| {
        let filter = std::env::var("RUST_LOG").unwrap_or_else(|_| default_filter.to_string());
        let _ = tracing_subscriber::registry()
            .with(tracing_subscriber::EnvFilter::new(filter))
            .with(tracing_subscriber::fmt::layer())
            .try_init();
    });
}

