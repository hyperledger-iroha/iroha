/// Nexus/Sora profile derived from `defaults/nexus/config.toml`.
#[derive(Debug, Clone)]
pub struct NexusProfile {
    pub lane_catalog: LaneCatalog,
    pub dataspace_catalog: DataSpaceCatalog,
    pub bootstrap_public_lanes: Vec<LaneId>,
    pub routing_policy: LaneRoutingPolicy,
    pub fusion: Fusion,
    pub commit: Commit,
    pub da: Da,
    pub da_enabled: bool,
    pub config_layer: Table,
}

fn canonical_addr_literal(addr: &str) -> Result<String> {
    let parsed: std::net::SocketAddr = addr
        .parse()
        .map_err(|err| eyre!("failed to parse embedded socket address `{addr}`: {err}"))?;
    Ok(IrohaSocketAddr::from(parsed).to_literal())
}

impl NexusProfile {
    /// Load the embedded Sora profile and expose both typed values and a TOML layer.
    pub fn sora_defaults() -> Result<Self> {
        const DEFAULT_CONFIG: &str = include_str!("../../../defaults/nexus/config.toml");
        let config_str = DEFAULT_CONFIG.replace("\\0", "");
        let mut raw_table: Table = toml::from_str(&config_str)
            .map_err(|err| eyre!("failed to parse embedded nexus config: {err}"))?;
        normalize_lane_metadata(&mut raw_table);
        let consensus_mode = Value::String("npos".to_string());
        if let Some(sumeragi) = raw_table.get_mut("sumeragi").and_then(Value::as_table_mut) {
            sumeragi.insert("consensus_mode".to_string(), consensus_mode);
        } else {
            let mut sumeragi = Table::new();
            sumeragi.insert("consensus_mode".to_string(), consensus_mode);
            raw_table.insert("sumeragi".to_string(), Value::Table(sumeragi));
        }
        let default_p2p_addr = canonical_addr_literal("127.0.0.1:1337")?;
        let default_torii_addr = canonical_addr_literal("127.0.0.1:8080")?;
        // Provide safe defaults for required network addresses and drop unsupported nested sections
        // so that deserialization succeeds without binding to real sockets.
        if let Some(network) = raw_table.get_mut("network").and_then(Value::as_table_mut) {
            network.insert(
                "address".to_string(),
                Value::String(default_p2p_addr.clone()),
            );
            network.insert(
                "public_address".to_string(),
                Value::String(default_p2p_addr.clone()),
            );
        } else {
            let mut network = Table::new();
            network.insert(
                "address".to_string(),
                Value::String(default_p2p_addr.clone()),
            );
            network.insert(
                "public_address".to_string(),
                Value::String(default_p2p_addr.clone()),
            );
            raw_table.insert("network".to_string(), Value::Table(network));
        }
        if let Some(torii) = raw_table.get_mut("torii").and_then(Value::as_table_mut) {
            torii.insert(
                "address".to_string(),
                Value::String(default_torii_addr.clone()),
            );
            torii.remove("sorafs");
        } else {
            let mut torii = Table::new();
            torii.insert(
                "address".to_string(),
                Value::String(default_torii_addr.clone()),
            );
            raw_table.insert("torii".to_string(), Value::Table(torii));
        }
        let actual = ConfigReader::new()
            .with_toml_source(TomlSource::inline(raw_table.clone()))
            .read_and_complete::<iroha_config::parameters::user::Root>()
            .map_err(|err| eyre!("failed to load embedded nexus config: {err:?}"))?
            .parse()
            .map_err(|err| eyre!("failed to parse embedded nexus config: {err:?}"))?;

        let nexus = actual.nexus.clone();
        let sumeragi = actual.sumeragi;
        let bootstrap_public_lanes = derive_bootstrap_public_lanes(&nexus);
        let config_layer = build_nexus_layer(&nexus, &sumeragi);

        Ok(Self {
            lane_catalog: nexus.lane_catalog,
            dataspace_catalog: nexus.dataspace_catalog,
            bootstrap_public_lanes,
            routing_policy: nexus.routing_policy,
            fusion: nexus.fusion,
            commit: nexus.commit,
            da: nexus.da,
            da_enabled: sumeragi.da.enabled,
            config_layer,
        })
    }
}

fn derive_bootstrap_public_lanes(nexus: &ActualNexus) -> Vec<LaneId> {
    let lanes: Vec<LaneId> = if nexus.lane_catalog.lanes().is_empty() {
        vec![LaneId::SINGLE]
    } else {
        nexus
            .lane_catalog
            .lanes()
            .iter()
            .map(|lane| lane.id)
            .collect()
    };

    lanes
        .into_iter()
        .filter(|lane| {
            matches!(
                nexus.staking.validator_mode(*lane, &nexus.lane_catalog),
                LaneValidatorMode::StakeElected
            )
        })
        .collect()
}

fn normalize_lane_metadata(raw: &mut Table) {
    if let Some(nexus) = raw.get_mut("nexus").and_then(Value::as_table_mut) {
        if let Some(Value::Array(lanes)) = nexus.get_mut("lane_catalog") {
            for lane in lanes {
                if let Some(table) = lane.as_table_mut() {
                    table
                        .entry("metadata".to_string())
                        .or_insert(Value::Table(Table::new()));
                }
            }
        }
    }
}

fn build_nexus_layer(nexus: &ActualNexus, sumeragi: &ActualSumeragi) -> Table {
    let mut layer = Table::new();
    TomlWriter::new(&mut layer).write(["nexus", "enabled"], true);
    TomlWriter::new(&mut layer).write(
        ["nexus", "lane_count"],
        i64::from(nexus.lane_catalog.lane_count().get()),
    );

    let dataspace_aliases = dataspace_alias_map(&nexus.dataspace_catalog);

    let lane_entries: Vec<Value> = nexus
        .lane_catalog
        .lanes()
        .iter()
        .map(|lane| {
            let mut entry = Table::new();
            entry.insert(
                "index".to_string(),
                Value::Integer(i64::from(lane.id.as_u32())),
            );
            entry.insert("alias".to_string(), Value::String(lane.alias.clone()));
            if let Some(description) = &lane.description {
                entry.insert(
                    "description".to_string(),
                    Value::String(description.clone()),
                );
            }
            entry.insert(
                "visibility".to_string(),
                Value::String(lane.visibility.as_str().to_string()),
            );
            entry.insert(
                "storage".to_string(),
                Value::String(lane.storage.as_str().to_string()),
            );
            entry.insert(
                "proof_scheme".to_string(),
                Value::String(lane.proof_scheme.as_str().to_string()),
            );
            if let Some(lane_type) = &lane.lane_type {
                entry.insert("lane_type".to_string(), Value::String(lane_type.clone()));
            }
            if let Some(governance) = &lane.governance {
                entry.insert("governance".to_string(), Value::String(governance.clone()));
            }
            if let Some(settlement) = &lane.settlement {
                entry.insert("settlement".to_string(), Value::String(settlement.clone()));
            }
            if let Some(dataspace) = dataspace_aliases.get(&lane.dataspace_id) {
                entry.insert("dataspace".to_string(), Value::String(dataspace.clone()));
            }
            let mut metadata = Table::new();
            for (key, value) in &lane.metadata {
                metadata.insert(key.clone(), Value::String(value.clone()));
            }
            entry.insert("metadata".to_string(), Value::Table(metadata));
            Value::Table(entry)
        })
        .collect();
    TomlWriter::new(&mut layer).write(["nexus", "lane_catalog"], Value::Array(lane_entries));

    let dataspace_entries: Vec<Value> = nexus
        .dataspace_catalog
        .entries()
        .iter()
        .map(|entry| {
            let mut value = Table::new();
            value.insert("alias".to_string(), Value::String(entry.alias.clone()));
            let id = i64::try_from(entry.id.as_u64()).unwrap_or_default();
            value.insert("id".to_string(), Value::Integer(id));
            if let Some(description) = &entry.description {
                value.insert(
                    "description".to_string(),
                    Value::String(description.clone()),
                );
            }
            Value::Table(value)
        })
        .collect();
    TomlWriter::new(&mut layer).write(
        ["nexus", "dataspace_catalog"],
        Value::Array(dataspace_entries),
    );

    TomlWriter::new(&mut layer).write(
        ["nexus", "routing_policy", "default_lane"],
        i64::from(nexus.routing_policy.default_lane.as_u32()),
    );
    if let Some(default_ds) = dataspace_aliases.get(&nexus.routing_policy.default_dataspace) {
        TomlWriter::new(&mut layer).write(
            ["nexus", "routing_policy", "default_dataspace"],
            default_ds.clone(),
        );
    }
    let rule_values: Vec<Value> = nexus
        .routing_policy
        .rules
        .iter()
        .map(|rule| lane_rule_to_value(rule, &dataspace_aliases))
        .collect();
    TomlWriter::new(&mut layer).write(
        ["nexus", "routing_policy", "rules"],
        Value::Array(rule_values),
    );

    TomlWriter::new(&mut layer).write(
        ["nexus", "fusion", "floor_teu"],
        i64::from(nexus.fusion.floor_teu),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "fusion", "exit_teu"],
        i64::from(nexus.fusion.exit_teu),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "fusion", "observation_slots"],
        i64::from(nexus.fusion.observation_slots.get()),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "fusion", "max_window_slots"],
        i64::from(nexus.fusion.max_window_slots.get()),
    );

    TomlWriter::new(&mut layer).write(
        ["nexus", "commit", "window_slots"],
        i64::from(nexus.commit.window_slots.get()),
    );

    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "q_in_slot_total"],
        i64::from(nexus.da.q_in_slot_total.get()),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "q_in_slot_per_ds_min"],
        i64::from(nexus.da.q_in_slot_per_ds_min.get()),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "sample_size_base"],
        i64::from(nexus.da.sample_size_base.get()),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "sample_size_max"],
        i64::from(nexus.da.sample_size_max.get()),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "threshold_base"],
        i64::from(nexus.da.threshold_base.get()),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "per_attester_shards"],
        i64::from(nexus.da.per_attester_shards.get()),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "audit", "sample_size"],
        i64::from(nexus.da.audit.sample_size.get()),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "audit", "window_count"],
        i64::from(nexus.da.audit.window_count.get()),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "audit", "interval_ms"],
        i64::try_from(nexus.da.audit.interval.as_millis()).unwrap_or_default(),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "recovery", "request_timeout_ms"],
        i64::try_from(nexus.da.recovery.request_timeout.as_millis()).unwrap_or_default(),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "rotation", "max_hits_per_window"],
        i64::from(nexus.da.rotation.max_hits_per_window.get()),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "rotation", "window_slots"],
        i64::from(nexus.da.rotation.window_slots.get()),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "rotation", "seed_tag"],
        nexus.da.rotation.seed_tag.clone(),
    );
    TomlWriter::new(&mut layer).write(
        ["nexus", "da", "rotation", "latency_decay"],
        nexus.da.rotation.latency_decay,
    );

    let consensus_mode = match sumeragi.consensus_mode {
        ConsensusMode::Permissioned => "permissioned",
        ConsensusMode::Npos => "npos",
    };
    TomlWriter::new(&mut layer)
        .write(["sumeragi", "consensus_mode"], consensus_mode)
        .write(["sumeragi", "da", "enabled"], sumeragi.da.enabled);

    layer
}

fn dataspace_alias_map(
    catalog: &DataSpaceCatalog,
) -> std::collections::BTreeMap<DataSpaceId, String> {
    catalog
        .entries()
        .iter()
        .map(|entry| (entry.id, entry.alias.clone()))
        .collect()
}

fn lane_rule_to_value(
    rule: &LaneRoutingRule,
    dataspace_aliases: &std::collections::BTreeMap<DataSpaceId, String>,
) -> Value {
    let mut table = Table::new();
    table.insert(
        "lane".to_string(),
        Value::Integer(i64::from(rule.lane.as_u32())),
    );
    if let Some(dataspace) = rule
        .dataspace
        .and_then(|id| dataspace_aliases.get(&id).cloned())
    {
        table.insert("dataspace".to_string(), Value::String(dataspace));
    }
    let mut matcher = Table::new();
    if let Some(account) = &rule.matcher.account {
        matcher.insert("account".to_string(), Value::String(account.to_string()));
    }
    if let Some(instruction) = &rule.matcher.instruction {
        matcher.insert(
            "instruction".to_string(),
            Value::String(instruction.clone()),
        );
    }
    if let Some(description) = &rule.matcher.description {
        matcher.insert(
            "description".to_string(),
            Value::String(description.clone()),
        );
    }
    table.insert("matcher".to_string(), Value::Table(matcher));
    Value::Table(table)
}

