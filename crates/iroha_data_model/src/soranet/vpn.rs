    #[derive(Debug, Clone, PartialEq, Eq, JsonSerialize, JsonDeserialize)]
    struct VpnVectorFixtureFile {
        flow_label_bits: u8,
        vectors: Vec<VpnVectorFixture>,
    }

    #[derive(Debug, Clone, PartialEq, Eq, JsonSerialize, JsonDeserialize)]
    struct VpnVectorFixture {
        name: String,
        class: String,
        flags: u8,
        circuit_id_hex: String,
        flow_label: u32,
        sequence: u64,
        ack: u64,
        padding_budget_ms: u16,
        payload_hex: String,
        frame_hex: String,
    }

    impl VpnVectorFixtureFile {
        fn path() -> PathBuf {
            PathBuf::from(env!("CARGO_MANIFEST_DIR")).join(FIXTURE_PATH)
        }

        fn load() -> Self {
            let expected = sample_fixture();
            let path = Self::path();
            let data = fs::read_to_string(&path).unwrap_or_else(|error| {
                panic!(
                    "failed to read vpn vector fixture {}: {error}",
                    path.display()
                )
            });
            let actual: VpnVectorFixtureFile =
                json::from_str(&data).expect("vpn vector fixture should parse");
            if actual != expected {
                let value = json::to_value(&expected)
                    .expect("vpn vector fixture should convert to json value");
                let mut rendered =
                    to_string_pretty(&value).expect("vpn vector fixture should render to json");
                rendered.push('\n');
                fs::write(&path, rendered).expect("failed to refresh vpn vector fixture");
                return expected;
            }
            actual
        }

        #[allow(dead_code)]
        fn render() -> String {
            let fixture = sample_fixture();
            let value =
                json::to_value(&fixture).expect("vpn vector fixture should convert to json value");
            let mut rendered =
                to_string_pretty(&value).expect("vpn vector fixture should render to json");
            rendered.push('\n');
            rendered
        }
    }

    impl VpnVectorFixture {
        fn class(&self) -> VpnCellClassV1 {
            match self.class.as_str() {
                "data" => VpnCellClassV1::Data,
                "cover" => VpnCellClassV1::Cover,
                "keep-alive" => VpnCellClassV1::KeepAlive,
                "control" => VpnCellClassV1::Control,
                other => panic!("unknown class label in fixture: {other}"),
            }
        }

        fn circuit_id(&self) -> [u8; 16] {
            let bytes = <Vec<u8>>::from_hex(&self.circuit_id_hex)
                .expect("fixture circuit id hex should decode");
            bytes
                .try_into()
                .expect("fixture circuit id must be 16 bytes")
        }

        fn payload(&self) -> Vec<u8> {
            <Vec<u8>>::from_hex(&self.payload_hex).expect("fixture payload hex should decode")
        }

        fn frame_bytes(&self) -> [u8; VPN_CELL_LEN] {
            let bytes =
                <Vec<u8>>::from_hex(&self.frame_hex).expect("fixture frame hex should decode");
            assert_eq!(
                bytes.len(),
                VPN_CELL_LEN,
                "fixture frame length mismatch: expected {}, found {}",
                VPN_CELL_LEN,
                bytes.len()
            );
            let mut frame = [0u8; VPN_CELL_LEN];
            frame.copy_from_slice(&bytes);
            frame
        }

        fn to_cell(&self) -> VpnCellV1 {
            let flow_label =
                VpnFlowLabelV1::from_u32(self.flow_label).expect("fixture flow label is valid");
            let header = VpnCellHeaderV1 {
                version: 1,
                class: self.class(),
                flags: VpnCellFlagsV1::from_bits(self.flags),
                circuit_id: self.circuit_id(),
                flow_label,
                sequence: self.sequence,
                ack: self.ack,
                padding_budget_ms: self.padding_budget_ms,
                payload_len: 0,
            };
            VpnCellV1 {
                header,
                payload: self.payload(),
            }
        }
    }

