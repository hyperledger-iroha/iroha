/// Enumeration of supported cell classes.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Decode, Encode, IntoSchema)]
pub enum VpnCellClassV1 {
    /// User traffic tunneled through the circuit.
    Data,
    /// Cover cell generated by the scheduler.
    Cover,
    /// Liveness probe without user payload.
    KeepAlive,
    /// Control-plane update (route push, key rotation).
    Control,
}

impl VpnCellClassV1 {
    /// Returns the on-wire tag for the class.
    #[must_use]
    pub const fn tag(self) -> u8 {
        match self {
            Self::Data => 0,
            Self::Cover => 1,
            Self::KeepAlive => 2,
            Self::Control => 3,
        }
    }
}

/// Bitfield describing cell handling hints.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Decode, Encode, IntoSchema)]
pub struct VpnCellFlagsV1 {
    bits: u8,
}

impl VpnCellFlagsV1 {
    /// Allowed flag mask (all known flags).
    pub const ALLOWED_MASK: u8 =
        Self::COVER | Self::REQUIRE_ACK | Self::MIGRATION | Self::KEY_ROTATION;
    /// Flag indicating that the frame carries cover traffic.
    pub const COVER: u8 = 0x01;
    /// Flag indicating that the frame should elicit an ACK.
    pub const REQUIRE_ACK: u8 = 0x02;
    /// Flag indicating that the cell belongs to a path-migration handshake.
    pub const MIGRATION: u8 = 0x04;
    /// Flag indicating a key rotation hint for the guard/exit.
    pub const KEY_ROTATION: u8 = 0x08;

    /// Construct a new flag bitfield from raw bits.
    #[must_use]
    pub const fn from_bits(bits: u8) -> Self {
        Self { bits }
    }

    /// Construct a new flag bitfield from individual hints.
    #[must_use]
    #[allow(clippy::fn_params_excessive_bools)]
    pub const fn new(cover: bool, require_ack: bool, migration: bool, key_rotation: bool) -> Self {
        let mut bits = 0;
        if cover {
            bits |= Self::COVER;
        }
        if require_ack {
            bits |= Self::REQUIRE_ACK;
        }
        if migration {
            bits |= Self::MIGRATION;
        }
        if key_rotation {
            bits |= Self::KEY_ROTATION;
        }
        Self { bits }
    }

    /// Returns the raw bitfield.
    #[must_use]
    pub const fn bits(self) -> u8 {
        self.bits
    }

    /// Returns `true` when any unknown flag bits are set.
    #[must_use]
    pub const fn has_unknown_bits(self) -> bool {
        (self.bits & !Self::ALLOWED_MASK) != 0
    }

    /// Returns `true` when the cover flag is set.
    #[must_use]
    pub const fn is_cover(self) -> bool {
        (self.bits & Self::COVER) != 0
    }
}

#[cfg(feature = "json")]
impl json::JsonSerialize for VpnCellFlagsV1 {
    fn json_serialize(&self, out: &mut String) {
        let _ = FmtWrite::write_fmt(out, format_args!("{}", self.bits));
    }
}

#[cfg(feature = "json")]
impl json::JsonDeserialize for VpnCellFlagsV1 {
    fn json_deserialize(parser: &mut json::Parser<'_>) -> Result<Self, json::Error> {
        let value = u64::json_deserialize(parser)?;
        if value > u64::from(u8::MAX) {
            return Err(json::Error::Message("flag value exceeds u8".into()));
        }
        let bits = u8::try_from(value)
            .map_err(|_| json::Error::Message("flag value exceeds u8".into()))?;
        Ok(Self::from_bits(bits))
    }
}

/// Flow label assigned to a tunnel.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Decode, Encode, IntoSchema)]
pub struct VpnFlowLabelV1 {
    /// Three-byte flow label encoded in network byte order.
    pub bytes: [u8; 3],
}

impl VpnFlowLabelV1 {
    /// Maximum allowed flow label value (24 bits).
    pub const MAX: u32 = 0x00FF_FFFF;
    /// Maximum supported flow label width.
    pub const MAX_BITS: u8 = 24;

    /// Construct a new flow label from a 24-bit integer.
    ///
    /// # Errors
    ///
    /// Returns [`VpnCellError::FlowLabelOverflow`] when `value` exceeds 24 bits.
    pub const fn from_u32(value: u32) -> Result<Self, VpnCellError> {
        if value > Self::MAX {
            return Err(VpnCellError::FlowLabelOverflow {
                value,
                max_bits: Self::MAX_BITS,
            });
        }
        let bytes = value.to_be_bytes();
        Ok(Self {
            bytes: [bytes[1], bytes[2], bytes[3]],
        })
    }

    /// Construct a new flow label bounded by `bits` (1–24).
    ///
    /// # Errors
    ///
    /// Returns [`VpnCellError::InvalidFlowLabelBits`] when `bits` exceeds the
    /// supported width or [`VpnCellError::FlowLabelOverflow`] when `value` does
    /// not fit into the requested width.
    pub fn from_u32_with_bits(value: u32, bits: u8) -> Result<Self, VpnCellError> {
        let max = Self::max_value_for_bits(bits)?;
        if value > max {
            return Err(VpnCellError::FlowLabelOverflow {
                value,
                max_bits: bits,
            });
        }
        let bytes = value.to_be_bytes();
        Ok(Self {
            bytes: [bytes[1], bytes[2], bytes[3]],
        })
    }

    /// Returns the maximum representable flow label for the supplied width.
    ///
    /// # Errors
    /// Returns [`VpnCellError::InvalidFlowLabelBits`] when `bits` is zero or exceeds
    /// the supported width.
    pub fn max_value_for_bits(bits: u8) -> Result<u32, VpnCellError> {
        if bits == 0 || bits > Self::MAX_BITS {
            return Err(VpnCellError::InvalidFlowLabelBits(bits));
        }
        if bits == Self::MAX_BITS {
            return Ok(Self::MAX);
        }
        Ok((1u32 << bits) - 1)
    }

    /// Ensures the flow label fits inside the supplied bit width.
    ///
    /// # Errors
    /// Returns [`VpnCellError::FlowLabelOverflow`] when the label exceeds the provided bit width.
    pub fn ensure_width(self, bits: u8) -> Result<(), VpnCellError> {
        let max = Self::max_value_for_bits(bits)?;
        if self.to_u32() > max {
            return Err(VpnCellError::FlowLabelOverflow {
                value: self.to_u32(),
                max_bits: bits,
            });
        }
        Ok(())
    }

    /// Decode the flow label into a 24-bit integer.
    #[must_use]
    pub const fn to_u32(self) -> u32 {
        u32::from_be_bytes([0, self.bytes[0], self.bytes[1], self.bytes[2]])
    }
}

#[cfg(feature = "json")]
impl json::JsonSerialize for VpnFlowLabelV1 {
    fn json_serialize(&self, out: &mut String) {
        let _ = FmtWrite::write_fmt(out, format_args!("{}", self.to_u32()));
    }
}

#[cfg(feature = "json")]
impl json::JsonDeserialize for VpnFlowLabelV1 {
    fn json_deserialize(parser: &mut json::Parser<'_>) -> Result<Self, json::Error> {
        let value = u32::json_deserialize(parser)?;
        let flow = Self::from_u32(value)
            .map_err(|err| json::Error::Message(format!("invalid flow label: {err}")))?;
        Ok(flow)
    }
}

/// Fixed header carried by every VPN cell.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Decode, Encode, IntoSchema)]
pub struct VpnCellHeaderV1 {
    /// Protocol version (currently `1`).
    pub version: u8,
    /// Cell class.
    pub class: VpnCellClassV1,
    /// Handling flags.
    pub flags: VpnCellFlagsV1,
    /// Circuit identifier derived from the route directory entry.
    #[cfg_attr(feature = "json", norito(with = "crate::json_helpers::fixed_bytes"))]
    pub circuit_id: [u8; 16],
    /// Flow label (24 bits).
    pub flow_label: VpnFlowLabelV1,
    /// Monotonic sequence number for replay protection.
    pub sequence: u64,
    /// Acknowledgement number (0 when unused).
    pub ack: u64,
    /// Padding budget in milliseconds for this cell.
    pub padding_budget_ms: u16,
    /// Payload length prior to padding.
    pub payload_len: u16,
}

impl VpnCellHeaderV1 {
    /// Returns the serialized header length.
    #[must_use]
    pub const fn serialized_len() -> usize {
        VPN_HEADER_LEN
    }
}

/// VPN cell with payload (unpadded).
#[derive(Debug, Clone, PartialEq, Eq, Decode, Encode, IntoSchema)]
pub struct VpnCellV1 {
    /// Cell header.
    pub header: VpnCellHeaderV1,
    /// Raw payload carried by the cell.
    #[cfg_attr(feature = "json", norito(with = "crate::json_helpers::bytes"))]
    pub payload: Vec<u8>,
}

impl VpnCellV1 {
    /// Maximum payload length (bytes) permitted in a single cell.
    #[must_use]
    pub const fn max_payload_len() -> usize {
        VPN_CELL_LEN - VPN_HEADER_LEN
    }

    /// Pad the cell into a fixed-length frame suitable for transport.
    ///
    /// # Errors
    ///
    /// Returns [`VpnCellError::PayloadTooLarge`] if the payload exceeds the
    /// available space after accounting for the header, or
    /// [`VpnCellError::PayloadLengthMismatch`] when `payload_len` would be
    /// truncated.
    pub fn into_padded_frame(mut self) -> Result<VpnPaddedCellV1, VpnCellError> {
        let max_payload = Self::max_payload_len();
        if self.payload.len() > max_payload {
            return Err(VpnCellError::PayloadTooLarge {
                max: max_payload,
                actual: self.payload.len(),
            });
        }
        let payload_len =
            u16::try_from(self.payload.len()).map_err(|_| VpnCellError::PayloadLengthMismatch {
                declared: u16::MAX,
                actual: self.payload.len(),
            })?;

        self.header.payload_len = payload_len;

        let mut frame = [0u8; VPN_CELL_LEN];
        let mut cursor = 0;
        frame[cursor] = self.header.version;
        cursor += 1;
        frame[cursor] = self.header.class.tag();
        cursor += 1;
        frame[cursor] = self.header.flags.bits();
        cursor += 1;
        frame[cursor..cursor + 16].copy_from_slice(&self.header.circuit_id);
        cursor += 16;
        frame[cursor..cursor + 3].copy_from_slice(&self.header.flow_label.bytes);
        cursor += 3;
        frame[cursor..cursor + 8].copy_from_slice(&self.header.sequence.to_be_bytes());
        cursor += 8;
        frame[cursor..cursor + 8].copy_from_slice(&self.header.ack.to_be_bytes());
        cursor += 8;
        frame[cursor..cursor + 2].copy_from_slice(&self.header.padding_budget_ms.to_be_bytes());
        cursor += 2;
        frame[cursor..cursor + 2].copy_from_slice(&self.header.payload_len.to_be_bytes());
        cursor += 2;

        frame[cursor..cursor + usize::from(payload_len)].copy_from_slice(&self.payload);

        Ok(VpnPaddedCellV1 { bytes: frame })
    }
}

/// Fixed-length padded cell frame.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct VpnPaddedCellV1 {
    /// Raw frame bytes (always [`VPN_CELL_LEN`] long).
    pub bytes: [u8; VPN_CELL_LEN],
}

impl AsRef<[u8]> for VpnPaddedCellV1 {
    fn as_ref(&self) -> &[u8] {
        &self.bytes
    }
}

impl VpnPaddedCellV1 {
    /// Parse a padded cell frame back into the header and payload components.
    ///
    /// # Errors
    ///
    /// Returns [`VpnCellError::FrameLengthMismatch`] if the frame length differs
    /// from the pinned cell size, [`VpnCellError::UnsupportedVersion`] or
    /// [`VpnCellError::InvalidClass`] for malformed headers,
    /// [`VpnCellError::InvalidFlags`] for unsupported flag bits, or
    /// [`VpnCellError::PayloadOverrun`] when the declared payload length exceeds
    /// the available bytes.
    pub fn parse(&self) -> Result<VpnCellV1, VpnCellError> {
        Self::parse_bytes(&self.bytes)
    }

    /// Parse a padded cell frame and enforce a maximum flow-label width.
    ///
    /// # Errors
    ///
    /// Returns [`VpnCellError::FlowLabelOverflow`] when the flow label exceeds
    /// `max_flow_label_bits`.
    pub fn parse_with_flow_label_bits(
        &self,
        max_flow_label_bits: u8,
    ) -> Result<VpnCellV1, VpnCellError> {
        Self::parse_bytes_with_flow_label_bits(&self.bytes, max_flow_label_bits)
    }

    /// Parse an arbitrary byte slice into a VPN cell with flow-label validation.
    ///
    /// # Errors
    ///
    /// Returns [`VpnCellError::FlowLabelOverflow`] when the flow label exceeds
    /// `max_flow_label_bits`.
    pub fn parse_bytes_with_flow_label_bits(
        bytes: &[u8],
        max_flow_label_bits: u8,
    ) -> Result<VpnCellV1, VpnCellError> {
        let cell = Self::parse_bytes(bytes)?;
        cell.header.flow_label.ensure_width(max_flow_label_bits)?;
        Ok(cell)
    }

    /// Parse an arbitrary byte slice into a VPN cell, expecting a full padded frame.
    ///
    /// # Errors
    /// Returns [`VpnCellError`] when the frame length, header, flags, payload length,
    /// or padding are invalid.
    pub fn parse_bytes(bytes: &[u8]) -> Result<VpnCellV1, VpnCellError> {
        if bytes.len() != VPN_CELL_LEN {
            return Err(VpnCellError::FrameLengthMismatch {
                expected: VPN_CELL_LEN,
                actual: bytes.len(),
            });
        }

        let version = bytes[0];
        if version != 1 {
            return Err(VpnCellError::UnsupportedVersion(version));
        }

        let class = match bytes[1] {
            0 => VpnCellClassV1::Data,
            1 => VpnCellClassV1::Cover,
            2 => VpnCellClassV1::KeepAlive,
            3 => VpnCellClassV1::Control,
            other => return Err(VpnCellError::InvalidClass(other)),
        };
        let flags = VpnCellFlagsV1::from_bits(bytes[2]);
        let cover_flag = flags.is_cover();
        if cover_flag != (class == VpnCellClassV1::Cover) {
            return Err(VpnCellError::FlagClassMismatch { class, flags });
        }
        if flags.has_unknown_bits() {
            return Err(VpnCellError::InvalidFlags {
                bits: flags.bits(),
                allowed: VpnCellFlagsV1::ALLOWED_MASK,
            });
        }

        let mut circuit_id = [0u8; 16];
        circuit_id.copy_from_slice(&bytes[3..19]);

        let flow_label = VpnFlowLabelV1 {
            bytes: [bytes[19], bytes[20], bytes[21]],
        };

        let sequence = u64::from_be_bytes(bytes[22..30].try_into().expect("slice length"));
        let ack = u64::from_be_bytes(bytes[30..38].try_into().expect("slice length"));
        let padding_budget_ms = u16::from_be_bytes(bytes[38..40].try_into().expect("slice length"));
        let payload_len = u16::from_be_bytes(bytes[40..42].try_into().expect("slice length"));
        let available_payload = bytes.len().saturating_sub(VPN_HEADER_LEN);
        let payload_len_usize = usize::from(payload_len);
        if payload_len_usize > VpnCellV1::max_payload_len() {
            return Err(VpnCellError::PayloadTooLarge {
                max: VpnCellV1::max_payload_len(),
                actual: payload_len_usize,
            });
        }
        if payload_len_usize > available_payload {
            return Err(VpnCellError::PayloadOverrun {
                declared: payload_len_usize,
                available: available_payload,
            });
        }

        let payload_end = VPN_HEADER_LEN + payload_len_usize;
        if payload_end < bytes.len()
            && let Some((offset, value)) = bytes[payload_end..]
                .iter()
                .copied()
                .enumerate()
                .find(|(_, byte)| *byte != 0)
        {
            return Err(VpnCellError::NonZeroPadding {
                index: payload_end + offset,
                value,
            });
        }
        let payload = bytes[VPN_HEADER_LEN..payload_end].to_vec();

        Ok(VpnCellV1 {
            header: VpnCellHeaderV1 {
                version,
                class,
                flags,
                circuit_id,
                flow_label,
                sequence,
                ack,
                padding_budget_ms,
                payload_len,
            },
            payload,
        })
    }
}

/// Cover traffic scheduler parameters.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Decode, Encode, IntoSchema)]
pub struct VpnCoverScheduleV1 {
    /// Ratio of cover cells to data cells expressed in permille (0–1000).
    pub cover_to_data_per_mille: u16,
    /// Heartbeat cadence for keepalives (milliseconds).
    pub heartbeat_ms: u16,
    /// Maximum burst of cover cells permitted per scheduling window.
    pub max_cover_burst: u16,
    /// Maximum jitter applied to cover spacing (milliseconds).
    pub jitter_ms: u16,
}

impl VpnCoverScheduleV1 {
    /// Generate a deterministic cover/data plan using the provided seed.
    #[must_use]
    pub fn plan(self, seed: [u8; 32], frames: usize) -> Vec<VpnCoverPlanEntryV1> {
        let mut prng = DeterministicPrng::new(seed);
        let mut entries = Vec::with_capacity(frames);
        let mut cover_streak = 0u16;
        for idx in 0..frames {
            let threshold = u16::try_from(prng.next_below(1_000)).unwrap_or(0);
            let mut is_cover = threshold < self.cover_to_data_per_mille;
            if is_cover {
                if cover_streak >= self.max_cover_burst {
                    is_cover = false;
                    cover_streak = 0;
                } else {
                    cover_streak = cover_streak.saturating_add(1);
                }
            } else {
                cover_streak = 0;
            }

            let jitter = u64::from(self.jitter_ms);
            let jitter_offset = if jitter == 0 {
                0
            } else {
                prng.next_below(jitter + 1)
            };
            let slot_ms = u64::from(self.heartbeat_ms);
            let slot_start = slot_ms.saturating_mul(idx as u64);
            let scheduled_ms = slot_start + jitter_offset;

            entries.push(VpnCoverPlanEntryV1 {
                slot_ms: scheduled_ms,
                is_cover,
            });
        }
        entries
    }
}

/// Deterministic PRNG seeded by a 32-byte buffer.
///
/// The generator streams bytes from a BLAKE3 XOF so all seed bits influence the
/// schedule while keeping the output stable across platforms.
struct DeterministicPrng {
    reader: blake3::OutputReader,
}

impl DeterministicPrng {
    fn new(seed: [u8; 32]) -> Self {
        let mut hasher = blake3::Hasher::new();
        hasher.update(b"soranet-vpn-cover-prng");
        hasher.update(&seed);
        Self {
            reader: hasher.finalize_xof(),
        }
    }

    fn next_u64(&mut self) -> u64 {
        let mut buf = [0u8; 8];
        self.reader.fill(&mut buf);
        u64::from_le_bytes(buf)
    }

    fn next_below(&mut self, upper: u64) -> u64 {
        if upper == 0 {
            return 0;
        }
        self.next_u64() % upper
    }
}

