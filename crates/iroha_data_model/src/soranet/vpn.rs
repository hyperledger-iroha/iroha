    #[allow(clippy::too_many_arguments)]
    fn build_vector(
        name: &str,
        class: VpnCellClassV1,
        flags: VpnCellFlagsV1,
        circuit_id: [u8; 16],
        flow_label: u32,
        sequence: u64,
        ack: u64,
        padding_budget_ms: u16,
        payload: Vec<u8>,
    ) -> VpnVectorFixture {
        let flow_label =
            VpnFlowLabelV1::from_u32(flow_label).expect("fixture flow label should fit into 24b");
        let header = VpnCellHeaderV1 {
            version: 1,
            class,
            flags,
            circuit_id,
            flow_label,
            sequence,
            ack,
            padding_budget_ms,
            payload_len: 0,
        };
        let cell = VpnCellV1 { header, payload };
        let frame = cell
            .clone()
            .into_padded_frame()
            .expect("fixture frame should pad");
        VpnVectorFixture {
            name: name.to_string(),
            class: class_label(class),
            flags: flags.bits(),
            circuit_id_hex: hex::encode(circuit_id),
            flow_label: flow_label.to_u32(),
            sequence,
            ack,
            padding_budget_ms,
            payload_hex: hex::encode(&cell.payload),
            frame_hex: hex::encode(frame.as_ref()),
        }
    }

    fn class_label(class: VpnCellClassV1) -> String {
        match class {
            VpnCellClassV1::Data => "data",
            VpnCellClassV1::Cover => "cover",
            VpnCellClassV1::KeepAlive => "keep-alive",
            VpnCellClassV1::Control => "control",
        }
        .to_string()
    }

