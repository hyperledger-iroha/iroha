/// Exit class advertised for billing/telemetry.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Decode, Encode, IntoSchema)]
pub enum VpnExitClassV1 {
    /// Standard exit class (balanced latency/bandwidth).
    Standard,
    /// Low-latency exit class.
    LowLatency,
    /// High-security exit class (higher cover, slower).
    HighSecurity,
}

/// Errors returned when parsing exit-class labels from config/client inputs.
#[derive(Debug, Clone, PartialEq, Eq, thiserror::Error)]
#[error("unknown VPN exit class label: {label}")]
pub struct VpnExitClassParseError {
    /// The label that failed validation.
    pub label: String,
}

impl VpnExitClassV1 {
    /// Parse a human-readable exit-class label into the enum, accepting the
    /// canonical labels and their hyphen/underscore variants.
    ///
    /// # Errors
    /// Returns [`VpnExitClassParseError`] when the label is unknown.
    pub fn try_from_label(label: &str) -> Result<Self, VpnExitClassParseError> {
        match label.to_ascii_lowercase().as_str() {
            "standard" => Ok(Self::Standard),
            "low-latency" | "low_latency" => Ok(Self::LowLatency),
            "high-security" | "high_security" => Ok(Self::HighSecurity),
            other => Err(VpnExitClassParseError {
                label: other.to_string(),
            }),
        }
    }

    /// Canonical hyphenated label for the exit class.
    #[must_use]
    pub const fn as_label(self) -> &'static str {
        match self {
            Self::Standard => "standard",
            Self::LowLatency => "low-latency",
            Self::HighSecurity => "high-security",
        }
    }
}

