/// Wrapper over [`RwaData`] used in storages.
pub type RwaValue = Owned<RwaData>;

impl RwaId {
    /// Create a generated RWA identifier inside the given domain.
    pub fn generated(domain: DomainId, hash: Hash) -> Self {
        Self { domain, hash }
    }
}

impl FromStr for RwaId {
    type Err = ParseError;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        let (hash_candidate, domain_id_candidate) = split_nonempty(
            s,
            '$',
            "RWA ID should have format `hash$domain`",
            "Empty `hash` part in `hash$domain`",
            "Empty `domain` part in `hash$domain`",
        )?;
        let hash = hash_candidate.parse().map_err(|_| ParseError {
            reason: "Failed to parse `hash` part in `hash$domain`",
        })?;
        let domain_id =
            DomainId::parse_fully_qualified(domain_id_candidate).map_err(|_| ParseError {
                reason: "Failed to parse `domain` part in `hash$domain`",
            })?;
        Ok(Self::new(domain_id, hash))
    }
}

impl Rwa {
    /// Construct a fully identified lot.
    #[allow(clippy::too_many_arguments)]
    pub fn new(
        id: RwaId,
        quantity: Numeric,
        spec: NumericSpec,
        primary_reference: String,
        status: Option<Name>,
        metadata: Metadata,
        parents: Vec<RwaParentRef>,
        controls: RwaControlPolicy,
        owned_by: AccountId,
    ) -> Self {
        Self {
            id,
            quantity,
            spec,
            primary_reference,
            status,
            metadata,
            parents,
            controls,
            owned_by,
            is_frozen: false,
            held_quantity: Numeric::zero(),
        }
    }

    /// Return the available quantity after subtracting held reservations.
    pub fn available_quantity(&self) -> Option<Numeric> {
        self.quantity
            .clone()
            .checked_sub(self.held_quantity.clone())
    }
}

impl NewRwa {
    /// Build a canonical lot from a generated identifier and owner.
    pub fn build(self, id: RwaId, owner: AccountId) -> Rwa {
        Rwa::new(
            id,
            self.quantity,
            self.spec,
            self.primary_reference,
            self.status,
            self.metadata,
            self.parents,
            self.controls,
            owner,
        )
    }
}

impl Identifiable for Rwa {
    type Id = RwaId;

    fn id(&self) -> &Self::Id {
        &self.id
    }
}

impl Registered for Rwa {
    type With = Self;
}

impl HasMetadata for Rwa {
    fn metadata(&self) -> &Metadata {
        &self.metadata
    }
}

impl HasMetadata for NewRwa {
    fn metadata(&self) -> &Metadata {
        &self.metadata
    }
}

impl IntoKeyValue for Rwa {
    type Key = RwaId;
    type Value = RwaValue;

    fn into_key_value(self) -> (Self::Key, Self::Value) {
        (
            self.id,
            Owned::new(RwaData {
                quantity: self.quantity,
                spec: self.spec,
                primary_reference: self.primary_reference,
                status: self.status,
                metadata: self.metadata,
                parents: self.parents,
                controls: self.controls,
                owned_by: self.owned_by,
                is_frozen: self.is_frozen,
                held_quantity: self.held_quantity,
            }),
        )
    }
}

#[cfg(feature = "json")]
impl mv::json::JsonKeyCodec for RwaId {
    fn encode_json_key(&self, out: &mut String) {
        norito::json::write_json_string(&self.to_string(), out);
    }

    fn decode_json_key(encoded: &str) -> Result<Self, norito::json::Error> {
        encoded
            .parse::<Self>()
            .map_err(|err| norito::json::Error::Message(err.to_string()))
    }
}

#[cfg(all(test, feature = "json"))]
mod json_tests {
    use super::*;

    #[test]
    fn new_rwa_json_roundtrip() {
        let builder = NewRwa::new(
            DomainId::try_new("vault", "universal").expect("domain"),
            "12.50".parse().expect("quantity"),
            NumericSpec::fractional(2),
            "https://example.test/certificates/lot-7".to_owned(),
            Some("vaulted".parse().expect("status")),
            Metadata::default(),
            Vec::new(),
            RwaControlPolicy::default(),
        );

        let json = norito::json::to_json(&builder).expect("serialize");
        let decoded: NewRwa = norito::json::from_json(&json).expect("deserialize");

        assert_eq!(decoded, builder);
    }
}

