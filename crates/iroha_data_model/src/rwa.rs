#[display("{hash}${domain}")]
#[getset(get = "pub")]
#[cfg_attr(
    feature = "json",
    derive(crate::DeriveJsonSerialize, crate::DeriveJsonDeserialize)
)]
#[cfg_attr(any(feature = "ffi_export", feature = "ffi_import"), ffi_type)]
pub struct RwaId {
    /// Domain that scopes the lot identifier.
    pub domain: DomainId,
    /// Canonical generated hash for the lot.
    pub hash: Hash,
}

/// Quantitative provenance edge from a parent lot.
#[derive(
    Debug, Clone, PartialEq, Eq, PartialOrd, Ord, Constructor, Getters, Decode, Encode, IntoSchema,
)]
#[getset(get = "pub")]
#[cfg_attr(
    feature = "json",
    derive(crate::DeriveJsonSerialize, crate::DeriveJsonDeserialize)
)]
#[cfg_attr(any(feature = "ffi_export", feature = "ffi_import"), ffi_type)]
pub struct RwaParentRef {
    /// Parent lot contributing to the child lot.
    pub rwa: RwaId,
    /// Quantity contributed by the parent lot.
    pub quantity: Numeric,
}

/// Per-lot controller configuration for optional compliance actions.
#[allow(clippy::struct_excessive_bools)] // Independent compliance toggles are kept explicit in the public data model.
#[derive(
    Debug, Clone, PartialEq, Eq, PartialOrd, Ord, Default, Getters, Decode, Encode, IntoSchema,
)]
#[getset(get = "pub")]
#[cfg_attr(
    feature = "json",
    derive(crate::DeriveJsonSerialize, crate::DeriveJsonDeserialize)
)]
#[cfg_attr(any(feature = "ffi_export", feature = "ffi_import"), ffi_type)]
pub struct RwaControlPolicy {
    /// Explicit controller accounts allowed to operate enabled controls on this lot.
    pub controller_accounts: Vec<AccountId>,
    /// Explicit controller roles allowed to operate enabled controls on this lot.
    pub controller_roles: Vec<RoleId>,
    /// Whether freezing is enabled for this lot.
    pub freeze_enabled: bool,
    /// Whether holds/reservations are enabled for this lot.
    pub hold_enabled: bool,
    /// Whether controller-driven forced transfer is enabled for this lot.
    pub force_transfer_enabled: bool,
    /// Whether redemption is enabled for this lot.
    pub redeem_enabled: bool,
}

/// Canonical real-world asset lot.
#[derive(
    Debug,
    Clone,
    Display,
    PartialEq,
    Eq,
    PartialOrd,
    Ord,
    Getters,
    MutGetters,
    Decode,
    Encode,
    IntoSchema,
)]
#[display("{id}")]
#[cfg_attr(
    feature = "json",
    derive(
        crate::DeriveFastJson,
        crate::DeriveJsonSerialize,
        crate::DeriveJsonDeserialize
    )
)]
#[cfg_attr(feature = "json", norito(no_fast_from_json))]
#[cfg_attr(any(feature = "ffi_export", feature = "ffi_import"), ffi_type)]
pub struct Rwa {
    /// Canonical identifier of the lot.
    pub id: RwaId,
    /// Current lot quantity. Partial transfers and redemptions reduce this value in place.
    #[getset(get = "pub", get_mut = "pub")]
    pub quantity: Numeric,
    /// Numeric policy constraining the quantity precision.
    #[getset(get_copy = "pub")]
    pub spec: NumericSpec,
    /// Primary off-chain reference describing the lot or certificate.
    #[getset(get = "pub")]
    pub primary_reference: String,
    /// Optional free-form status label.
    #[getset(get = "pub")]
    pub status: Option<Name>,
    /// Metadata carried by the lot.
    #[getset(get = "pub", get_mut = "pub")]
    pub metadata: Metadata,
    /// Quantitative parent edges describing provenance.
    #[getset(get = "pub", get_mut = "pub")]
    pub parents: Vec<RwaParentRef>,
    /// Optional compliance controls for the lot.
    #[getset(get = "pub", get_mut = "pub")]
    pub controls: RwaControlPolicy,
    /// Account that currently owns the lot.
    #[getset(get = "pub", get_mut = "pub")]
    pub owned_by: AccountId,
    /// Whether the lot is currently frozen.
    #[getset(get_copy = "pub", get_mut = "pub")]
    pub is_frozen: bool,
    /// Reserved quantity that cannot be transferred, merged, or redeemed.
    #[getset(get = "pub", get_mut = "pub")]
    pub held_quantity: Numeric,
}

