fn pattern_frame(dimensions: (u16, u16), seed: u8) -> Vec<u8> {
    let (w, h) = dimensions;
    let count = usize::from(w) * usize::from(h);
    (0..count)
        .map(|idx| seed.wrapping_add((idx as u8).wrapping_mul(7)))
        .collect()
}

