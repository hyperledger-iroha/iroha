#[test]
fn baseline_encoder_matches_golden() {
    let dimensions = (8, 8);
    let frame_dimensions = FrameDimensions::new(dimensions.0, dimensions.1);
    let mut encoder = BaselineEncoder::new(BaselineEncoderConfig {
        frame_dimensions,
        frame_duration_ns: 41_666_667,
        frames_per_segment: 3,
        layer_bitmap: 0b101,
        encryption_suite: EncryptionSuite::X25519ChaCha20Poly1305([0xAB; 32]),
        duration_ns: 0,
        quantizer: 0,
        ..BaselineEncoderConfig::default()
    });

    let frames = [
        pattern_frame(dimensions, 0x11),
        pattern_frame(dimensions, 0x37),
        pattern_frame(dimensions, 0x59),
    ];
    let raw_frames: Vec<_> = frames
        .iter()
        .map(|bytes| RawFrame::new(frame_dimensions, bytes.clone()).expect("frame"))
        .collect();

    let segment = encoder
        .encode_segment(41, 1_333_000, 1_024, &raw_frames, None)
        .expect("encode segment");

    assert_eq!(segment.chunks.len(), 3);
    let expected_chunks = [
        EXPECTED_CHUNK_0.as_slice(),
        EXPECTED_CHUNK_1.as_slice(),
        EXPECTED_CHUNK_2.as_slice(),
    ];
    for (index, (chunk, expected)) in segment
        .chunks
        .iter()
        .zip(expected_chunks.iter())
        .enumerate()
    {
        assert_eq!(
            chunk.as_slice(),
            *expected,
            "chunk {index} payload mismatch"
        );
    }

    assert_eq!(segment.descriptors.len(), 3);
    for (idx, (descriptor, expected_commitment)) in segment
        .descriptors
        .iter()
        .zip(EXPECTED_COMMITMENTS.iter())
        .enumerate()
    {
        assert_eq!(descriptor.chunk_id, idx as u16);
        assert_eq!(descriptor.length as usize, expected_chunks[idx].len());
        assert_eq!(
            descriptor.offset as usize,
            expected_chunks[..idx].iter().map(|c| c.len()).sum()
        );
        assert_eq!(
            &descriptor.commitment, expected_commitment,
            "chunk {idx} commitment mismatch"
        );
    }

    assert_eq!(
        chunk_commitments(
            41,
            &segment
                .descriptors
                .iter()
                .zip(segment.chunks.iter())
                .map(|(descriptor, chunk)| (descriptor.chunk_id, chunk.as_slice()))
                .collect::<Vec<_>>()
        ),
        EXPECTED_COMMITMENTS,
        "chunk commitments mismatch"
    );

    assert_eq!(segment.header.nonce_salt, EXPECTED_NONCE_SALT);
}
