#!/usr/bin/env bash
set -euo pipefail

usage() {
    cat <<'USAGE'
run_sorafs_gateway_probe.sh [options] -- <sorafs-gateway-probe args>

Runs `cargo xtask sorafs-gateway-probe`, forces `--report-json` to land in
`artifacts/sorafs_gateway_probe/`, and (optionally) wires the JSON summary +
logs into the SoraFS drill log, PagerDuty, and rollback hooks so
paging/rollback drills have automation coverage.

Script options (pass probe flags after `--`):
  --workspace <PATH>              Repository root (defaults to script location).
  --scenario <NAME>               Drill scenario recorded in ops/drill-log.md.
  --drill-log <PATH>              Drill log path (default: <workspace>/ops/drill-log.md).
  --date <YYYY-MM-DD>             Override drill date (UTC). Defaults to now.
  --start <HH:MMZ>                Override drill start time (UTC).
  --end <HH:MMZ>                  Override drill end time (UTC). Defaults to now.
  --ic <NAME>                     Incident commander recorded in the drill log.
  --scribe <NAME>                 Scribe recorded in the drill log.
  --notes <TEXT>                  Extra notes appended to the autogenerated entry.
  --link <URL>                    Incident or evidence link recorded in the drill log.
  --artifact-dir <PATH>           Directory for probe logs (default: artifacts/sorafs_gateway_probe).
  --rollback-plan <TEXT>          Reference to the rollback playbook (added to notes/custom details).
  --rollback-hook <PATH>          Command executed when the probe fails (before exit).
  --rollback-hook-arg <VALUE>     Argument forwarded to the rollback hook (repeatable).

PagerDuty integration:
  --pagerduty-routing-key <KEY>   Routing key used to trigger incidents (or set PAGERDUTY_ROUTING_KEY).
  --pagerduty-summary <TEXT>      Override the incident summary line.
  --pagerduty-source <TEXT>       Sets `payload.source` (default: sorafs-gateway-probe).
  --pagerduty-component <TEXT>    Sets `payload.component` (default: sorafs-gateway).
  --pagerduty-group <TEXT>        Sets `payload.group` (default: tls-drill).
  --pagerduty-severity <LEVEL>    One of info|warning|error|critical (default: critical).
  --pagerduty-dedup-key <KEY>     Optional deduplication key.
  --pagerduty-detail key=value    Extra custom_details pair (repeatable).
  --pagerduty-output <PATH>       Where to store the JSON payload (default: artefacts dir).
  --pagerduty-url <URL>           Override the Events API endpoint (default: https://events.pagerduty.com/v1/enqueue).
  --pagerduty-dry-run             Skip the HTTP POST; still writes the payload for evidence.

Any arguments appearing after `--` are passed verbatim to
`cargo xtask sorafs-gateway-probe`.
USAGE
}

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../.." && pwd)"
LOG_SCRIPT="${SCRIPT_DIR}/log_sorafs_drill.sh"

WORKSPACE="${REPO_ROOT}"
SCENARIO=""
DATE_OVERRIDE=""
START_OVERRIDE=""
END_OVERRIDE=""
IC_NAME=""
SCRIBE_NAME=""
NOTES_EXTRA=""
LINK_VALUE=""
DRILL_LOG=""
ARTIFACT_DIR=""
ROLLBACK_PLAN=""
ROLLBACK_HOOK=""
ROLLBACK_HOOK_ARGS=()

PAGE_ROUTING_KEY="${PAGERDUTY_ROUTING_KEY:-}"
PAGE_SUMMARY=""
PAGE_SEVERITY="critical"
PAGE_SOURCE="sorafs-gateway-probe"
PAGE_COMPONENT="sorafs-gateway"
PAGE_GROUP="tls-drill"
PAGE_DEDUP_KEY=""
PAGE_URL="${PAGERDUTY_EVENT_URL:-https://events.pagerduty.com/v1/enqueue}"
PAGE_DRY_RUN=0
PAGE_OUTPUT_PATH=""
PD_EXTRA_DETAILS=()

while [[ $# -gt 0 ]]; do
    case "$1" in
        --workspace)
            WORKSPACE="$(cd "$2" && pwd)"
            shift 2
            ;;
        --scenario)
            SCENARIO="$2"
            shift 2
            ;;
        --drill-log)
            DRILL_LOG="$2"
            shift 2
            ;;
        --date)
            DATE_OVERRIDE="$2"
            shift 2
            ;;
        --start)
            START_OVERRIDE="$2"
            shift 2
            ;;
        --end)
            END_OVERRIDE="$2"
            shift 2
            ;;
        --ic)
            IC_NAME="$2"
            shift 2
            ;;
        --scribe)
            SCRIBE_NAME="$2"
            shift 2
            ;;
        --notes)
            NOTES_EXTRA="$2"
            shift 2
            ;;
        --link)
            LINK_VALUE="$2"
            shift 2
            ;;
        --artifact-dir)
            ARTIFACT_DIR="$2"
            shift 2
            ;;
        --rollback-plan)
            ROLLBACK_PLAN="$2"
            shift 2
            ;;
        --rollback-hook)
            ROLLBACK_HOOK="$2"
            shift 2
            ;;
        --rollback-hook-arg)
            ROLLBACK_HOOK_ARGS+=("$2")
            shift 2
            ;;
        --pagerduty-routing-key)
            PAGE_ROUTING_KEY="$2"
            shift 2
            ;;
        --pagerduty-summary)
            PAGE_SUMMARY="$2"
            shift 2
            ;;
        --pagerduty-source)
            PAGE_SOURCE="$2"
            shift 2
            ;;
        --pagerduty-component)
            PAGE_COMPONENT="$2"
            shift 2
            ;;
        --pagerduty-group)
            PAGE_GROUP="$2"
            shift 2
            ;;
        --pagerduty-severity)
            PAGE_SEVERITY="$2"
            shift 2
            ;;
        --pagerduty-dedup-key)
            PAGE_DEDUP_KEY="$2"
            shift 2
            ;;
        --pagerduty-detail)
            PD_EXTRA_DETAILS+=("$2")
            shift 2
            ;;
        --pagerduty-output)
            PAGE_OUTPUT_PATH="$2"
            shift 2
            ;;
        --pagerduty-url)
            PAGE_URL="$2"
            shift 2
            ;;
        --pagerduty-dry-run)
            PAGE_DRY_RUN=1
            shift 1
            ;;
        --help|-h)
            usage
            exit 0
            ;;
        --)
            shift
            break
            ;;
        *)
            echo "Unknown option: $1" >&2
            usage >&2
            exit 1
            ;;
    esac
done

if [[ $# -eq 0 ]]; then
    echo "error: pass sorafs-gateway-probe arguments after --" >&2
    usage >&2
    exit 1
fi

PROBE_ARGS=("$@")
WORKSPACE="$(cd "$WORKSPACE" && pwd)"
ARTIFACT_DIR="${ARTIFACT_DIR:-${WORKSPACE}/artifacts/sorafs_gateway_probe}"
mkdir -p "${ARTIFACT_DIR}"

timestamp="$(date -u +%Y%m%dT%H%M%SZ)"
log_path="${ARTIFACT_DIR}/probe_${timestamp}.log"
page_output_default="${ARTIFACT_DIR}/pagerduty_${timestamp}.json"

report_arg=""
for ((idx = 0; idx < ${#PROBE_ARGS[@]}; idx++)); do
    if [[ "${PROBE_ARGS[idx]}" == "--report-json" ]]; then
        if (( idx + 1 >= ${#PROBE_ARGS[@]} )); then
            echo "error: --report-json requires a path argument" >&2
            exit 1
        fi
        report_arg="${PROBE_ARGS[idx + 1]}"
        break
    fi
done

if [[ -z "${report_arg}" ]]; then
    json_report="${ARTIFACT_DIR}/probe_${timestamp}.json"
    PROBE_ARGS+=("--report-json" "${json_report}")
else
    if [[ "${report_arg}" == "-" ]]; then
        echo "error: run_sorafs_gateway_probe.sh requires --report-json to target a file path" >&2
        exit 1
    fi
    json_report="${report_arg}"
fi

run_date="$(date -u +%F)"
log_date="${DATE_OVERRIDE:-${run_date}}"
run_start="$(date -u +%H:%MZ)"
log_start="${START_OVERRIDE:-${run_start}}"

echo "Running cargo xtask sorafs-gateway-probe ${PROBE_ARGS[*]}"
set +e
(
    cd "${WORKSPACE}"
    cargo xtask sorafs-gateway-probe "${PROBE_ARGS[@]}"
) 2>&1 | tee "${log_path}"
probe_status=${PIPESTATUS[0]}
set -e

probe_summary=""
primary_failure=""
probe_host=""
probe_manifest=""
if [[ -f "${json_report}" ]]; then
    if command -v python3 >/dev/null 2>&1; then
        probe_meta_output="$(
            python3 - "${json_report}" <<'PY'
import json
import sys

path = sys.argv[1]
try:
    with open(path, "r", encoding="utf-8") as handle:
        data = json.load(handle)
except Exception:
    print()
    print()
    print()
    print()
    sys.exit(0)

failures = data.get("failures") or []
if failures:
    summary = "failures: " + ", ".join(failures[:3])
    primary = failures[0]
else:
    summary = "all checks passed"
    primary = ""

host = data.get("host", "")
manifest = ""
gar_block = data.get("gar")
if isinstance(gar_block, dict):
    manifest = gar_block.get("manifest_cid", "") or ""

print(summary)
print(primary)
print(host)
print(manifest)
PY
        )"
        IFS=$'\n' read -r probe_summary primary_failure probe_host probe_manifest <<EOF
${probe_meta_output}
EOF
        probe_summary="${probe_summary:-}"
        primary_failure="${primary_failure:-}"
        probe_host="${probe_host:-}"
        probe_manifest="${probe_manifest:-}"
    else
        echo "warning: python3 not found; skipping probe JSON summary parse" >&2
    fi
else
    echo "warning: probe JSON ${json_report} not found" >&2
fi

log_end_value="${END_OVERRIDE:-$(date -u +%H:%MZ)}"
status_str="pass"
if [[ ${probe_status} -ne 0 ]]; then
    status_str="fail"
fi

notes_value="Probe log: ${log_path}; report: ${json_report}"
if [[ -n "${ROLLBACK_PLAN}" ]]; then
    notes_value+="; rollback: ${ROLLBACK_PLAN}"
fi
if [[ -n "${probe_summary}" ]]; then
    notes_value+="; summary: ${probe_summary}"
fi
if [[ -n "${NOTES_EXTRA}" ]]; then
    notes_value+="; ${NOTES_EXTRA}"
fi

if [[ -n "${SCENARIO}" ]]; then
    if [[ ! -x "${LOG_SCRIPT}" ]]; then
        echo "error: helper not found at ${LOG_SCRIPT}" >&2
        exit 1
    fi
    drill_log_path="${DRILL_LOG:-${WORKSPACE}/ops/drill-log.md}"
    log_args=(--scenario "${SCENARIO}" --status "${status_str}" --notes "${notes_value}" --log "${drill_log_path}" --date "${log_date}" --start "${log_start}" --end "${log_end_value}")
    [[ -n "${IC_NAME}" ]] && log_args+=(--ic "${IC_NAME}")
    [[ -n "${SCRIBE_NAME}" ]] && log_args+=(--scribe "${SCRIBE_NAME}")
    [[ -n "${LINK_VALUE}" ]] && log_args+=(--link "${LINK_VALUE}")
    "${LOG_SCRIPT}" "${log_args[@]}"
fi

trigger_pagerduty() {
    local routing_key="$1"
    local summary="$2"
    local source="$3"
    local severity="$4"
    local component="$5"
    local group="$6"
    local dedup="$7"
    local timestamp_rfc3339="$8"
    local log_file="$9"
    local scenario="$10"
    local notes="$11"
    local link="$12"
    local rollback_plan="$13"
    local status_value="$14"
    local host_value="$15"
    local manifest_value="$16"
    local payload

    if ! command -v python3 >/dev/null 2>&1; then
        echo "error: python3 is required to format PagerDuty payloads" >&2
        return 1
    fi
    payload="$(
        python3 - "$routing_key" "$summary" "$source" "$severity" "$component" "$group" "$dedup" "$timestamp_rfc3339" "$log_file" "$scenario" "$notes" "$link" "$rollback_plan" "$status_value" "$host_value" "$manifest_value" -- "${PD_EXTRA_DETAILS[@]}" <<'PY'
import json
import sys

args = sys.argv[1:]
if '--' in args:
    sep = args.index('--')
    base = args[:sep]
    extras = args[sep + 1:]
else:
    base = args
    extras = []

if len(base) != 16:
    raise SystemExit(f"expected 16 parameters, received {len(base)}")

(
    routing_key,
    summary,
    source,
    severity,
    component,
    group,
    dedup,
    timestamp_rfc3339,
    log_file,
    scenario,
    notes,
    link,
    rollback_plan,
    status_value,
    host_value,
    manifest_value,
) = base

custom_details = {
    "status": status_value,
    "probe_log": log_file,
}
if scenario:
    custom_details["scenario"] = scenario
if notes:
    custom_details["notes"] = notes
if link:
    custom_details["drill_link"] = link
if rollback_plan:
    custom_details["rollback_plan"] = rollback_plan
if host_value:
    custom_details["host"] = host_value
if manifest_value:
    custom_details["manifest_cid"] = manifest_value

for pair in extras:
    if not pair or "=" not in pair:
        continue
    key, value = pair.split("=", 1)
    custom_details[key] = value

payload = {
    "routing_key": routing_key,
    "event_action": "trigger",
    "client": "sorafs-gateway-probe",
    "payload": {
        "summary": summary,
        "source": source or "sorafs-gateway-probe",
        "severity": severity or "critical",
        "component": component or "sorafs-gateway",
        "group": group or "tls-drill",
        "timestamp": timestamp_rfc3339,
        "custom_details": custom_details,
    },
}

if dedup:
    payload["dedup_key"] = dedup

print(json.dumps(payload))
PY
    )"

    if [[ -z "${payload}" ]]; then
        echo "error: failed to render PagerDuty payload" >&2
        return 1
    fi

    local target="${PAGE_OUTPUT_PATH:-${page_output_default}}"
    mkdir -p "$(dirname "${target}")"
    printf '%s\n' "${payload}" > "${target}"
    echo "Stored PagerDuty payload at ${target}"

    if [[ ${PAGE_DRY_RUN} -eq 1 ]]; then
        echo "PagerDuty dry-run enabled; skipping POST"
        return 0
    fi

    if ! command -v curl >/dev/null 2>&1; then
        echo "error: curl is required to send PagerDuty events" >&2
        return 1
    fi

    curl -fsS -X POST "${PAGE_URL}" \
        -H 'Content-Type: application/json' \
        --data-binary @"${target}" >/dev/null
    echo "PagerDuty event sent via ${PAGE_URL}"
}

if [[ "${status_str}" == "fail" && -n "${PAGE_ROUTING_KEY}" ]]; then
    summary_default="SoraFS gateway probe failed"
    if [[ -n "${SCENARIO}" ]]; then
        summary_default+=" (${SCENARIO})"
    fi
    if [[ -n "${primary_failure}" ]]; then
        summary_default+=": ${primary_failure}"
    fi
    summary_msg="${PAGE_SUMMARY:-${summary_default}}"
    event_timestamp="$(date -u +%Y-%m-%dT%H:%M:%SZ)"
    trigger_pagerduty "${PAGE_ROUTING_KEY}" "${summary_msg}" "${PAGE_SOURCE}" "${PAGE_SEVERITY}" \
        "${PAGE_COMPONENT}" "${PAGE_GROUP}" "${PAGE_DEDUP_KEY}" "${event_timestamp}" \
        "${log_path}" "${SCENARIO}" "${notes_value}" "${LINK_VALUE}" "${ROLLBACK_PLAN}" "${status_str}" \
        "${probe_host}" "${probe_manifest}"
fi

if [[ "${status_str}" == "fail" && -n "${ROLLBACK_HOOK}" ]]; then
    echo "Executing rollback hook: ${ROLLBACK_HOOK} ${ROLLBACK_HOOK_ARGS[*]}"
    "${ROLLBACK_HOOK}" "${ROLLBACK_HOOK_ARGS[@]}"
fi

echo "Gateway probe status: ${status_str} (log: ${log_path}, report: ${json_report})"

exit "${probe_status}"
